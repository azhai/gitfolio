export function t(key) {
  var parts = key.split('.')
  var obj = zh
  for (var i = 0; i < parts.length; i++) {
    if (obj == null) return key
    obj = obj[parts[i]]
  }
  return obj != null ? obj : key
}

export function timeAgo(dateStr) {
  if (!dateStr) return ''
  var d = new Date(dateStr)
  var diff = (Date.now() - d.getTime()) / 1000
  if (diff < 10) return zh.time.justNow
  if (diff < 60) return Math.floor(diff) + zh.time.secondsAgo
  if (diff < 3600) return Math.floor(diff / 60) + zh.time.minutesAgo
  if (diff < 86400) return Math.floor(diff / 3600) + zh.time.hoursAgo
  if (diff < 604800) return Math.floor(diff / 86400) + zh.time.daysAgo
  return d.toLocaleDateString('zh-CN')
}

