export function t(key) {
  var parts = key.split('.')
  var obj = en
  for (var i = 0; i < parts.length; i++) {
    if (obj == null) return key
    obj = obj[parts[i]]
  }
  return obj != null ? obj : key
}

export function timeAgo(dateStr) {
  if (!dateStr) return ''
  var d = new Date(dateStr)
  var diff = (Date.now() - d.getTime()) / 1000
  if (diff < 10) return en.time.justNow
  if (diff < 60) return Math.floor(diff) + en.time.secondsAgo
  if (diff < 3600) return Math.floor(diff / 60) + en.time.minutesAgo
  if (diff < 86400) return Math.floor(diff / 3600) + en.time.hoursAgo
  if (diff < 604800) return Math.floor(diff / 86400) + en.time.daysAgo
  return d.toLocaleDateString('en-US')
}

